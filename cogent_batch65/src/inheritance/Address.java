package inheritance;

public class Address {
	
	int HouseNo;
	String CityName;
	int StreetNo;
	
	public int getHouseNo() {
		return HouseNo;
	}
	public void setHouseNo(int houseNo) {
		HouseNo = houseNo;
	}
	public String getCityName() {
		return CityName;
	}
	public void setCityName(String cityName) {
		CityName = cityName;
	}
	public int getStreetNo() {
		return StreetNo;
	}
	public void setStreetNo(int streetNo) {
		StreetNo = streetNo;
	}

}
